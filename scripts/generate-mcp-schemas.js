// Generates JSON Schema for the MCP server's tool input shapes directly from
// src/types.ts, so `inputSchema` on each registered tool is never
// hand-maintained and can't drift from the actual TypeScript types (TC-117).
//
// Usage:
//   node scripts/generate-mcp-schemas.js [sourcePath] [outputPath]
//
// Both arguments are optional and default to the real project paths. They
// exist so tests can point the generator at a fixture copy of types.ts
// without touching the real generated output (see test/mcp/schema-drift
// coverage for TC-117 step 3).
import { createGenerator } from "ts-json-schema-generator";
import * as fs from "node:fs";
import * as path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.join(__dirname, "..");

const sourcePath = process.argv[2]
  ? path.resolve(process.argv[2])
  : path.join(repoRoot, "src/types.ts");

const outputPath = process.argv[3]
  ? path.resolve(process.argv[3])
  : path.join(repoRoot, "src/mcp/schemas.generated.ts");

// Tool-wrapper shapes — these are what src/mcp/server.ts's `inputSchema`
// fields actually consume.
const TOOL_SCHEMAS = [
  {
    constName: "parseTransactionsInputSchema",
    typeName: "ParseTransactionsInput",
  },
  {
    constName: "classifyInstrumentsInputSchema",
    typeName: "ClassifyInstrumentsInput",
  },
  { constName: "calculateGainsInputSchema", typeName: "CalculateGainsInput" },
];

// Shared domain types referenced by the tool-wrapper shapes above. Generated
// as standalone consts too, so they're available for direct comparison
// against types.ts in tests without re-deriving them from the wrapper
// schemas' `$ref`/`definitions`.
const SHARED_SCHEMAS = [
  { constName: "transactionSchema", typeName: "Transaction" },
  { constName: "classificationMapSchema", typeName: "ClassificationMap" },
  { constName: "classificationEntrySchema", typeName: "ClassificationEntry" },
  { constName: "assetClassSchema", typeName: "AssetClass" },
  { constName: "carryForwardSchema", typeName: "CarryForward" },
  { constName: "incomeRowSchema", typeName: "IncomeRow" },
];

const generator = createGenerator({
  path: sourcePath,
  tsconfig: path.join(repoRoot, "tsconfig.json"),
  type: "*",
  skipTypeCheck: true,
  // Inline the root type's `type`/`properties`/`required` at the top level
  // instead of wrapping it in a `$ref`. The MCP SDK's `Tool.inputSchema`
  // (see @modelcontextprotocol/sdk/types.js) requires those 3 keys directly
  // on the schema object — a top-level `$ref` doesn't satisfy it. Nested
  // referenced types (e.g. Transaction inside an array) still resolve via
  // `$ref`/`definitions` as usual.
  topRef: false,
});

const entries = [...TOOL_SCHEMAS, ...SHARED_SCHEMAS].map(
  ({ constName, typeName }) => ({
    constName,
    schema: generator.createSchema(typeName),
  }),
);

const header =
  "// AUTO-GENERATED by scripts/generate-mcp-schemas.js from src/types.ts.\n" +
  "// Do not edit by hand — run `node scripts/generate-mcp-schemas.js` to regenerate.\n\n";

const body = entries
  .map(
    ({ constName, schema }) =>
      `export const ${constName} = ${JSON.stringify(schema, null, 2)} as const;\n`,
  )
  .join("\n");

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, header + body);

console.log(
  `Wrote ${entries.length} schema(s) to ${path.relative(repoRoot, outputPath)}`,
);
